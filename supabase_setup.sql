        END IF;
    END IF;
END $$;

-- 2. Ensure shipments table has all required columns
-- This fixes the "create shipment is not working" issue.
DO $$ 
BEGIN
    -- Create shipments table if it doesn't exist
    CREATE TABLE IF NOT EXISTS shipments (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        date TIMESTAMP WITH TIME ZONE DEFAULT NOW() NOT NULL,
        status TEXT DEFAULT 'pending' NOT NULL,
        user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE
    );

    -- Create shipment_items table if it doesn't exist
    CREATE TABLE IF NOT EXISTS shipment_items (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        shipment_id BIGINT REFERENCES shipments(id) ON DELETE CASCADE,
        product_id BIGINT REFERENCES products(id),
        quantity INTEGER NOT NULL
    );

    -- Add columns if they are missing
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='shipments' AND column_name='supplier_name') THEN
        ALTER TABLE shipments ADD COLUMN supplier_name TEXT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='shipments' AND column_name='total_cost') THEN
        ALTER TABLE shipments ADD COLUMN total_cost NUMERIC DEFAULT 0;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='shipments' AND column_name='shipping_cost') THEN
        ALTER TABLE shipments ADD COLUMN shipping_cost NUMERIC DEFAULT 0;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='shipments' AND column_name='description') THEN
        ALTER TABLE shipments ADD COLUMN description TEXT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='shipments' AND column_name='weight_kg') THEN
        ALTER TABLE shipments ADD COLUMN weight_kg NUMERIC;
    END IF;
END $$;

